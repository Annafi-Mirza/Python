#
# Piecewise Kernel Density Estimation
#
#  inputs:
#      X        list or 1D np-array of data X-coordinates
#      Y        list or 1D np-array of data Y-coordinates
#      npieces  number of line segments to fit to the dataset
#  outputs:
#      return xout,yout
#
#      xout     output x-coordinates of the fitted line segments
#      yout     output y-coordinates of the fitted line segments
#
#---------------------------

def piecewise_kde(X,Y,npiece):
  X = np.array(X)
  Y = np.array(Y)

  # what are x0 and x1
  x0 = np.min(X)
  x1 = np.max(X)

  # what is sigma
  sigma = 0.25 * (x1-x0) / npiece
  root2pi = 2.50662827463

  # What are the points
  npoint = npiece+1
  xout = np.linspace(x0,x1,npoint)

  # Run the gaussian function over each point
  yout = np.zeros((npoint,), dtype='float64')
  for i in range(npoint):
    mu = xout[i]
    weight = 1.0/(sigma*root2pi) * np.exp(-0.5 * (X-mu)*(X-mu)/(sigma*sigma))
    total = np.sum(Y * weight)
    count = np.sum(weight)
    yout[i] = total/count

  return xout,yout

